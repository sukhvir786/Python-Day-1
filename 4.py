"""
    Arithmetic Operators
"""
x = int(input("Enter First integer value :"))
y = int(input("Enter Second integer value :"))
print("Sum = ",x+y)
print("Difference = ",x-y)
print("Product = ",x*y)
print("Division = ",round (x/y,2))
print("Quotient = ",x//y)
print("Remainder = ",x%y)
print(x," to the power ",y," is :",x**y)
